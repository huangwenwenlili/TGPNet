            self.test()

            visuals = self.get_current_visuals()
            restored = torch.clamp(self.output, 0, 1)[:,0:3]
            metric_data_met['img'] = restored.cpu().detach()[:,0:3].permute(0, 2, 3, 1).squeeze(0).numpy()
            if 'gt' in visuals:
                metric_data_met['img2'] = self.gt.cpu().detach()[:,0:3].permute(0, 2, 3, 1).squeeze(0).numpy()

            sr_img = tensor2img([visuals['result']], rgb2bgr=True)
            sr_img1 = tensor2img([visuals['result']], out_type=np.float_, rgb2bgr=rgb2bgr)
            metric_data['img'] = sr_img
            if 'gt' in visuals:
                gt_img = tensor2img([visuals['gt']], rgb2bgr=True)
